# Алгоритм проверки строки на палиндром:
# 1. Преобразовать строку в нижний регистр для исключения влияния регистра букв.
# 2. Удалить из строки пробелы, чтобы избежать влияния пробелов на проверку.
# 3. Развернуть строку, используя срез с отрицательным шагом.
# 4. Сравнить исходную обработанную строку с её перевернутым вариантом.
# 5. Если они совпадают, вернуть True (строка является палиндромом).
# 6. Если строки различаются, вернуть False.

def is_palindrome(s):
    """
    Проверяет, является ли строка палиндромом.
    
    :param s: Входная строка
    :return: True, если строка палиндром, иначе False
    """
    s = s.lower().replace(" ", "")  # Приведение к нижнему регистру и удаление пробелов
    return s == s[::-1]  # Проверка совпадения строки с перевернутой версией

# Примеры использования функции
print(is_palindrome("А роза упала на лапу Азора"))  # Вывод: True
print(is_palindrome("Привет"))  # Вывод: False
